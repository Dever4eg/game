    <h4>Как опыт влияет на уровень, таблица уровней и опыта </h4>
    <p>По мере приобретения опыта, Ваш уровень повышается.</p>
    <br>

<h4>Возможности на уровнях </h4>
    <p>1 уровень: доступные - ресурс булыжник, торговля, лес, шахта <p>
    <p>3 уровень: доступные - бои с реальными соперниками в меню "Бои"</p>
   <p>7 уровень: доступная - ферма</p>
    <p>10 уровень: доступный - клан</p>
    <p>11 уровень: доступный - ресурсы уголь, красная пыль, железо</p>
    <p>28 уровень: доступный - ресурс золото</p>
    <p>36 уровень: доступный - ресурс алмаз</p>
    
