    <p>Опыт <img class="characteristic" src="/img/characteristic/XP_orb_animation.gif" alt=""> - система условных обозначений достижений игрока, выраженная в числовом эквиваленте (очках опыта). Игрок получает очки опыта, которые выпадают из убитых мобов и игроков. Также можно добыть опыт из руды (кроме железа и золота), переплавкой руд, обжигом глины, песка, булыжника, кактуса, древесины, жаркой мяса, рыбы, разведения животных, ловли рыбы, разбив пузырёк зелья опыта.  
